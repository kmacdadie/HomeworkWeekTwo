public class loops {
	
	public static void main(String[] args) {
		
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}
		
		System.out.println("loop is over.");
		
		int i = 0;
	
		while (i < 10) {
			System.out.println("1 is less then 10. i = " + i);
			i++;
		}
		
		do {
			System.out.println("Do while:" + i);
		} while (i < 10);

}
		
}
